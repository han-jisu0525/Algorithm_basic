package basic5_1;

import java.util.Scanner;

public class total_1407 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        String x = sc.nextLine();
        String[] n = x.split(" ");

        for (int i = 0; i < n.length; i++) {
            System.out.print(n[i]);
        }
    }
}
